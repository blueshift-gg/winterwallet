#[test]
fn derive_matches_canonical_scalars() {
    let kp = WinternitzKeypair::from_mnemonic(MNEMONIC, 0).expect("valid");
    let sk: WinternitzPrivkey<16> = kp.derive();
    assert_eq!(sk.as_bytes(), &EXPECTED_PRIVKEY_W0_P0_C0[..]);
}

